struct S {
    ll sum, sz;
};
S op(S a, S b) {
    return {a.sum + b.sum, a.sz + b.sz};
}

S e() {
    return {0, 0};
}

struct F {
    ll add;
};
S mapping(F a, S b) {
    return {b.sum + b.sz * a.add, b.sz};
}

F composition(F a, F b) {
    return {b.add + a.add};
}

F id() {
    return {0};
}

